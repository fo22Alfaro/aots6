class ComplexTorusLattice:
    """
    Complex torus T^n_C = C^n / Λ defined by a period matrix Ω.

    For n=3 (AOTS6 case):
      Ω is a 3×3 complex matrix with Im(Ω) > 0 (positive definite).
      The lattice Λ = Z^3 + Ω*Z^3.
      
    The Riemann conditions for T^n_C to be an Abelian variety (projective):
      (a) Ω is symmetric
      (b) Im(Ω) is positive definite
    
    An Abelian variety has the richest Hodge structure and is where
    the Hodge conjecture is most tractable.
    """

    def __init__(self, n: int = 3, seed: int = 42):
        self.n   = n
        rng      = np.random.default_rng(seed)
        # Build a valid period matrix: Ω = A + i*B, B > 0, Ω = Ω^T
        A = rng.uniform(-0.5, 0.5, (n, n))
        A = (A + A.T) / 2                         # symmetrize
        B_half = rng.uniform(0.1, 1.0, (n, n))
        B = B_half @ B_half.T + 0.1 * np.eye(n)  # positive definite
        self.Omega = A + 1j * B

    def is_abelian_variety(self) -> bool:
        """Check Riemann conditions: Ω symmetric, Im(Ω) > 0."""
        sym_ok = np.allclose(self.Omega, self.Omega.T, atol=1e-10)
        B      = np.imag(self.Omega)
        evals  = np.real(linalg.eigvalsh(B))
        pos_ok = np.all(evals > 0)
        return bool(sym_ok and pos_ok)

    def intersection_form(self) -> np.ndarray:
        """
        Hermitian intersection form E on H^{1,0}.
        E(u, v) = (Im Ω)^{-1} u* v  (positive definite on H^{1,0})
        """
        B_inv = np.real(linalg.inv(np.imag(self.Omega)))
        return B_inv

    def first_chern_class(self) -> np.ndarray:
        """
        First Chern class of the principal polarization:
        c_1(L) ∈ H^{1,1} represented as antisymmetric real matrix.
        For the principal polarization: c_1 = Im(Ω)^{-1}.
        """
        return np.real(linalg.inv(np.imag(self.Omega)))

    def algebraic_1_cycles(self) -> List[np.ndarray]:
        """
        H^{1,1} algebraic classes for the abelian variety.
        By the Lefschetz (1,1) theorem: all H^{1,1} classes are algebraic
        for an abelian variety with maximal endomorphism algebra.
        Returns basis of H^{1,1} as matrices.
        """
        n = self.n
        basis = []
        for i in range(n):
            for j in range(n):
                M = np.zeros((n, n), dtype=complex)
                M[i, j] = 1.0
                basis.append(M)
        return basis

    def hodge_class_check(self, cohom_class: np.ndarray) -> Dict[str, Any]:
        """
        Check if a given cohomology class is a Hodge class (type p,p).
        For H^{1,1}: the class α is of type (1,1) if
          α(v, w) = 0 whenever v, w ∈ H^{1,0}
        """
        # Simple check: is it in H^{1,1} (Hermitian matrix)?
        is_hermitian = np.allclose(cohom_class, cohom_class.conj().T, atol=1e-10)
        return {
            "is_hodge_class": bool(is_hermitian),
            "type":           "(1,1)" if is_hermitian else "mixed",
        }

    def summary(self) -> Dict[str, Any]:
        abelian = self.is_abelian_variety()
        c1      = self.first_chern_class()
        return {
            "n":                  self.n,
            "is_abelian_variety": abelian,
            "period_matrix_Re":   np.round(np.real(self.Omega), 4).tolist(),
            "period_matrix_Im":   np.round(np.imag(self.Omega), 4).tolist(),
            "Im_Omega_eigenvals": np.round(
                np.real(linalg.eigvalsh(np.imag(self.Omega))), 4
            ).tolist(),
            "c1_trace":           round(float(np.trace(c1)), 4),
            "H11_dim":            self.n ** 2,
            "lefschetz_theorem":  "All H^{1,1} classes are algebraic (PROVEN)",
        }


