    print(f"    0-cycle type : {point_cls['hodge_type']} — {point_cls['reason']}")
    print(f"    Divisor type : {div_cls['hodge_type']} — {div_cls['reason']}")
    results["aots6_mapping"] = {
        "point_class":   point_cls,
        "divisor_class": div_cls,
    }

    ms_total = (time.perf_counter() - t_total) * 1000
    print(f"\n" + "─" * 64)
    print(f"  Hodge exploration complete in {ms_total:.1f}ms")
    print(f"  Lefschetz (1,1) theorem: VERIFIED numerically")
    print(f"  Hodge conjecture (p≥2): OPEN — requires formal algebraic geometry proof")
    print("=" * 64 + "\n")

    return results


