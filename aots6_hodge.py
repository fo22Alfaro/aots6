    print(" MP-06: HODGE CONJECTURE — Computational Exploration")
    print(" T^6 = T^3_C (complex 3-torus) — the AOTS6 manifold")
    print("=" * 64)

    results = {}
    t_total = time.perf_counter()

    # 1. Hodge numbers
    t0  = time.perf_counter()
    hn  = HodgeNumbers(n=3)
    s1  = hn.summary()
    ms1 = (time.perf_counter() - t0) * 1000
    print(f"\n  [Hodge Numbers of T^3_C]  ({ms1:.1f}ms)")
    tbl = np.array(s1["hodge_diamond"])
    print(f"    Hodge diamond (p=rows, q=cols):")
