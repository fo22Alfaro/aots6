# 1. HODGE NUMBERS OF COMPLEX TORI
# ─────────────────────────────────────────────────────────────────────────────

class HodgeNumbers:
    """
    Hodge numbers h^{p,q}(T^{2n}_C) for a complex n-torus.

    For T^{2n}_C = C^n / Λ:
      h^{p,q} = C(n,p) * C(n,q)  (binomial coefficients)
      
    This is because H^1(T^{2n}_C) is n-dimensional over C,
    and all cohomology is generated by wedge products of H^1.
    """

    def __init__(self, n: int = 3):
        """
        n = complex dimension of the torus.
        For AOTS6: n=3 gives T^6_C = T^6 (real), our manifold.
        """
        self.n = n
        self._table = None

    def binom(self, n: int, k: int) -> int:
        if k < 0 or k > n:
            return 0
        if k == 0 or k == n:
            return 1
        k = min(k, n - k)
        result = 1
        for i in range(k):
            result = result * (n - i) // (i + 1)
        return result

    def h(self, p: int, q: int) -> int:
        """h^{p,q}(T^{2n}_C) = C(n,p) * C(n,q)."""
        return self.binom(self.n, p) * self.binom(self.n, q)

    def table(self) -> np.ndarray:
        """Full Hodge diamond (n+1) × (n+1)."""
        if self._table is None:
            self._table = np.array([[self.h(p, q)
                                     for q in range(self.n + 1)]
                                    for p in range(self.n + 1)])
        return self._table

    def betti_number(self, k: int) -> int:
        """b_k = Σ_{p+q=k} h^{p,q}."""
        return sum(self.h(p, k - p) for p in range(k + 1)
                   if 0 <= p <= self.n and 0 <= k - p <= self.n)

    def euler_characteristic(self) -> int:
        """χ = Σ_k (-1)^k * b_k. For tori: χ = 0."""
        return sum((-1) ** k * self.betti_number(k)
                   for k in range(2 * self.n + 1))

    def hodge_class_dimension(self, p: int) -> int:
        """dim H^{p,p}(T^{2n}_C, Q) = h^{p,p} = C(n,p)^2."""
        return self.h(p, p)

    def summary(self) -> Dict[str, Any]:
        tbl = self.table()
        return {
            "n_complex":          self.n,
            "real_dimension":     2 * self.n,
            "hodge_diamond":      tbl.tolist(),
            "betti_numbers":      [self.betti_number(k)
                                   for k in range(2 * self.n + 1)],
            "euler_char":         self.euler_characteristic(),
            "H11_dim":            self.hodge_class_dimension(1),
            "H22_dim":            self.hodge_class_dimension(2),
            "H33_dim":            self.hodge_class_dimension(3),
            "H11_algebraic":      "ALL (Lefschetz theorem — PROVEN)",
            "H22_algebraic":      "OPEN (Hodge conjecture for p=2)",
        }


