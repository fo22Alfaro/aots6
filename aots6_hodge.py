    print(f"    Euler char     : {s1['euler_char']}")
    print(f"    dim H^{{1,1}}    : {s1['H11_dim']}  → {s1['H11_algebraic']}")
    print(f"    dim H^{{2,2}}    : {s1['H22_dim']}  → {s1['H22_algebraic']}")
    results["hodge_numbers"] = s1

    # 2. Period matrix
    t0  = time.perf_counter()
    lat = ComplexTorusLattice(n=3)
    s2  = lat.summary()
    ms2 = (time.perf_counter() - t0) * 1000
    print(f"\n  [Period Matrix T^3_C]  ({ms2:.1f}ms)")
    print(f"    is_abelian_variety : {s2['is_abelian_variety']}")
    print(f"    Im(Ω) eigenvalues  : {s2['Im_Omega_eigenvals']}")
    print(f"    Lefschetz theorem  : {s2['lefschetz_theorem']}")
    results["lattice"] = s2

    # 3. Hodge decomposition on T^6
    t0  = time.perf_counter()
    hd  = HodgeDecompositionT6(n_real=6)
    s3  = hd.summary()
    ms3 = (time.perf_counter() - t0) * 1000
    print(f"\n  [Hodge Decomposition on T^6]  ({ms3:.1f}ms)")
    print(f"    Betti numbers : {s3['betti_numbers']}")
    print(f"    Euler char    : {s3['euler_char']}")
    print(f"    dim H^{{1,1}}  : {s3['H11_hodge_dim']}")
    print(f"    dim H^{{2,2}}  : {s3['H22_hodge_dim']}")
    print(f"    Hodge conj.   : {s3['hodge_conj_status']}")
    results["decomposition"] = s3

    # 4. Algebraic cycle detector
    t0   = time.perf_counter()
    det  = AlgebraicCycleDetector(lat)
    s4   = det.analyze()
    ms4  = (time.perf_counter() - t0) * 1000
    print(f"\n  [Algebraic Cycle Analysis]  ({ms4:.1f}ms)")
    print(f"    H^{{1,1}} basis size   : {s4['H11_basis_size']}")
    print(f"    H^{{1,1}} all integral : {s4['H11_all_integral']}")
    print(f"    Lefschetz (1,1)       : {s4['lefschetz_holds']}")
    print(f"    H^{{2,2}} candidates  : {s4['H22_candidates']}")
    print(f"    H^{{2,2}} integral    : {s4['H22_integral']}")
    print(f"    Status                : {s4['status']}")
    results["cycles"] = s4

    # 5. AOTS6 ↔ Hodge mapping
    t0   = time.perf_counter()
    ahm  = AOTS6HodgeMapping()
