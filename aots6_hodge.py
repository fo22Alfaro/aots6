class AOTS6HodgeMapping:
    """
    Map between AOTS6 T^6 nodes and Hodge classes on T^3_C.

    The six dimensions of T^6 in AOTS6 correspond to the six real
    coordinates of T^3_C = R^6/Z^6:
      (x_1, x_2)  ↔  complex coordinate z_1 = x_1 + i*x_2  (D0, D1)
      (x_3, x_4)  ↔  complex coordinate z_2 = x_3 + i*x_4  (D2, D3)
      (x_5, x_6)  ↔  complex coordinate z_3 = x_5 + i*x_6  (D4, D5)

    A node at position c ∈ T^6 defines a point on T^3_C.
    Its Hodge class is the current [δ_c] ∈ H^{3,3}(T^3_C).
    """

    def t6_to_complex(self, coord: List[float]) -> np.ndarray:
        """T^6 coordinates → complex coordinates on T^3_C."""
        c  = [x % 1.0 for x in coord]
        z1 = c[0] + 1j * c[1]
        z2 = c[2] + 1j * c[3]
        z3 = c[4] + 1j * c[5]
        return np.array([z1, z2, z3])

    def holomorphic_form_value(self, coord: List[float]) -> complex:
        """
        Value of the holomorphic 3-form Ω = dz_1 ∧ dz_2 ∧ dz_3
        evaluated at the point z(coord).
        On a flat torus, Ω is constant = 1.
        """
        return 1.0 + 0j

    def node_hodge_class(self, coord: List[float]) -> Dict[str, Any]:
        """
        The Hodge class of the 0-cycle (point) at coord.
        A point is always algebraic (Hodge conj. trivially holds for p=n=3).
        """
        z = self.t6_to_complex(coord)
        return {
            "complex_coords": [round(float(np.real(zi)), 4) +
                               round(float(np.imag(zi)), 4) * 1j
                               for zi in z],
            "hodge_type":     "(3,3)",
            "is_algebraic":   True,
            "reason":         "All H^{3,3} classes are algebraic (trivially)",
            "cycle_type":     "0-dimensional (point)",
        }

    def divisor_class(self, coord: List[float],
                      normal_dir: int = 0) -> Dict[str, Any]:
        """
        A divisor (complex codimension 1 subvariety) through coord.
        Divisors are elements of H^{1,1} — algebraic by Lefschetz.
        """
        z    = self.t6_to_complex(coord)
        c1   = np.zeros((3, 3))
        c1[normal_dir, normal_dir] = 1.0
        return {
            "hodge_type":    "(1,1)",
            "is_algebraic":  True,
            "reason":        "Lefschetz (1,1) theorem — all H^{1,1} algebraic",
            "c1_class":      c1.tolist(),
            "normal_dir":    f"z_{normal_dir+1}",
        }


